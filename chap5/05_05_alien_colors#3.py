"""
I assign the string 'green' to the variable `alien_color` and check its value using a series of conditional statements. 
Since it equals 'green', I print a message indicating that 5 points have been earned. 
Next, I assign 'yellow' to `alien_color` and check again; this time, I print a message indicating that 10 points have been earned because the condition matches 'yellow'. 
Finally, I assign 'red' to `alien_color` and check its value once more, printing a message indicating that 15 points have been earned for matching 'red'.
"""

alien_color = 'green'
if alien_color == 'green':
    print('You just earned 5 points!')
elif alien_color == 'yellow':
    print("You just earned 10 points")
elif alien_color == 'red':
    print("You just earned 15 points")

alien_color = 'yellow'
if alien_color == 'green':
    print('You just earned 5 points!')
elif alien_color == 'yellow':
    print("You just earned 10 points")
elif alien_color == 'red':
    print("You just earned 15 points")

alien_color = 'red'
