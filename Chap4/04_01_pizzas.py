"""
I create a list called `pizzas` containing 'buffalo', 'pepperoni', and 'stuffed crust'. 
I use a for loop to iterate over each type of pizza and print a statement expressing my love for each one. 
Finally, I print a message emphasizing my fondness for pizza, particularly buffalo pizza, explaining that it's my favorite because of my love for buffalo sauce.
"""

pizzas = ['buffalo','pepperoni','stuffed crust'] 
for pizza in pizzas:
    print(f"I love {pizza.title()} pizza") 
print("I really like pizza. Especially buffalo pizza. It is my favorite just because I love buffalo sauce. " + "\n I really love pizza")
