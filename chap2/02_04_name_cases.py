"""
I assign the string "ian reedy" to the variable `name`. 
Then, I print three variations of `name`: 
- `name.lower()` prints it in all lowercase: "ian reedy".
- `name.upper()` prints it in all uppercase: "IAN REEDY".
- `name.title()` capitalizes the first letter of each word: "Ian Reedy".
"""


name = "ian reedy"

print(name.lower())  
print(name.upper())  
print(name.title())  
